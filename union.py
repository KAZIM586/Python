#Union
A={1,2,3}
B={4,5,6}
print(A|B)
print(A.union(B))



# intersection

A={1,2,3,4}
B={1,2,5,6,8}
print(A&B)
print(A.intersection(B))

# Difference

A={1,2,3}
B={1,2,6}
print(A-B)
print(A.difference(B))




# Symmetric_difference

A={1,2,3,4,5}
B={1,2,3,4,5,6,78}
print(A^B)
print(A.symmetric_difference(B))





